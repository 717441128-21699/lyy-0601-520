const generateDemoNotes = (chartId: string, bpm: number, duration: number, density: number): Note[] => {
  const notes: Note[] = [];
  const beatInterval = 60 / bpm;
  const totalBeats = Math.floor(duration / beatInterval);
  const notesPerBeat = density;
  
  for (let i = 0; i < totalBeats; i++) {
    const baseTime = i * beatInterval + 0.05;
    const notesThisBeat = Math.floor(Math.random() * notesPerBeat) + 1;
    
    for (let j = 0; j < notesThisBeat; j++) {
      const trackIndex = Math.floor(Math.random() * 4);
      const time = baseTime + (j * beatInterval / notesPerBeat);
      const noteTypes: Note['type'][] = ['tap', 'tap', 'tap', 'hold', 'slide'];
      const type = noteTypes[Math.floor(Math.random() * noteTypes.length)];
      
      notes.push({
        id: generateId(),
        chartId,
        trackIndex,
        time: Math.round(time * 1000) / 1000,
        type,
        duration: type === 'hold' || type === 'slide' ? Math.random() * 0.5 + 0.2 : undefined,
        hitSound: 'normal',
      });
    }
  }
  
  return notes.sort((a, b) => a.time - b.time);
};

